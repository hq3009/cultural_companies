-- 创建 multiclass 表，包含 multiclass.csv 的所有字段，indv_nm 作为主键
-- 该表用于存储文旅企业的行业分类信息，包含行业代码、企业类型、文旅标识等关键信息
-- 数据来源于 multiclass.csv 文件，行业代码基于《国民经济行业分类》标准
CREATE TABLE IF NOT EXISTS multiclass (
    industry VARCHAR(10) COMMENT '行业大类代码（A-农、林、牧、渔业，C-制造业等）',
    daleidaima VARCHAR(10) COMMENT '行业大类代码，与industry字段含义相同',
    zhongleidaima VARCHAR(10) COMMENT '行业中类代码，表示行业分类的中间层级',
    indv_nm VARCHAR(50) PRIMARY KEY COMMENT '行业小类代码，作为主键，表示最细粒度的行业分类',
    wh_menleidaima VARCHAR(10) COMMENT '文旅门类代码，表示在文旅分类体系中的门类（A-文化核心领域，B-文化相关领域）',
    wh_xiaoleidaima VARCHAR(10) COMMENT '文旅小类代码，表示文旅分类体系中的具体小类',
    is_core VARCHAR(10) COMMENT '是否为核心文旅企业（1-是，0-否）',
    is_all_wh VARCHAR(10) COMMENT '是否为全部文旅企业（1-是，0-否）',
    is_new_wh VARCHAR(10) COMMENT '是否为新文旅企业（1-是，0-否）',
    ly_xiaoleidaima VARCHAR(10) COMMENT '旅游小类代码，表示旅游分类体系中的具体小类',
    is_all_ly VARCHAR(10) COMMENT '是否为全部旅游企业（1-是，0-否）',
    type VARCHAR(50) COMMENT '企业类型（culture-文化企业，tourism-旅游企业）',
    xinyetai VARCHAR(10) COMMENT '新业态标识（1-是，0-否）',
    shengbiao VARCHAR(10) COMMENT '省标标识（1-是，0-否）'
);

-- 为表添加注释
COMMENT ON TABLE multiclass IS '文旅企业分类表，包含行业分类代码和企业类型信息，用于识别和分类文旅相关企业';

